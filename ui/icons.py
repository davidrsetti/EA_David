def icon(name: str, size: int = 18, color: str = "currentColor", stroke: float = 1.75) -> str:
    """Return an inline Lucide SVG string. Use in markdown with unsafe_allow_html=True."""
    body = _LUCIDE.get(name)
    if not body:
        return ""
    return (
        f'<span class="nx-icon">'
        f'<svg width="{size}" height="{size}" viewBox="0 0 24 24" fill="none" '
        f'stroke="{color}" stroke-width="{stroke}" stroke-linecap="round" stroke-linejoin="round">'
        f'{body}</svg></span>'
    )


# ── Material Symbols shortcodes (used by st.tabs, buttons, metrics) ──────
# Streamlit 1.36+ renders ":material/<name>:" as a Material icon.
def mat(name: str) -> str:
    return f":material/{name}:"


